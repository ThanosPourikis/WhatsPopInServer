Create TABLE EVENT (
	EVENTID		INT	PRIMARY	KEY	NOT	NULL,
	NAME		TEXT	NOT NULL,
	PLACE		TEXT,
	CATEGORY	TEXT,
	DESCRIPTION	TEXT,
	CREATORID	INT	NOT	NULL,
	IMAGE		TEXT,
	DATE		REAL,
	TIME		TEXT
);
CREATE TABLE USERS(
	EMAIL		TEXT PRIMARY KEY NOT NULL,
	PASSWORD	TEXT NOT NULL

);
CREATE TABLE ATTENDS(
	EVENTID	INT	REFERENCES EVENT(EVENTID),
	USERID	INT	REFERENCES USERS(USERID)

)